    private boolean userNamePatternCorrect(String userName){
        Pattern p;
        p = Pattern.compile("^[a-zA-Z0-9]+_[a-zA-Z0-9]+@[a-zA-Z0-9]+.[a-zA-Z0-9]+$");
        Matcher m = p.matcher(userName);
        boolean b= m.matches();
        return b;
    }
